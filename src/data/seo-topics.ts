export function getAllTopics(): (SEOTopic & { servicePillar: string; pillarName: string })[] {
  return SERVICE_PILLARS.flatMap(p => p.topics.map(t => ({ ...t, servicePillar: p.id, pillarName: p.name })));
}

export function getTopicsByPillar(pillarId: string): SEOTopic[] {
  return SERVICE_PILLARS.find(p => p.id === pillarId)?.topics ?? [];
}

export function getTopicById(topicId: string): (SEOTopic & { servicePillar: string; pillarName: string }) | undefined {
  for (const pillar of SERVICE_PILLARS) {
    const topic = pillar.topics.find(t => t.id === topicId);
    if (topic) return { ...topic, servicePillar: pillar.id, pillarName: pillar.name };
  }
  return undefined;
}
